    private void setupInitialDate(JSpinner dateSpinner) {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MMM-yyyy");
        String currentDate = LocalDate.now().format(formatter);
        setupDateSpinner(dateSpinner, currentDate);
    }
    
    
    private void setupDateSpinner(JSpinner dateSpinner, String dateString) {
        if (dateSpinner == null) {
            throw new IllegalArgumentException("Spinner cannot be null");
        }
        if (dateString == null || dateString.trim().isEmpty()) {
            throw new IllegalArgumentException("Date string cannot be empty");
        }
        try {
            SimpleDateFormat formatter = new SimpleDateFormat("dd-MMM-yyyy");
            formatter.setLenient(false);
            java.util.Date parsedDate = formatter.parse(dateString);
            SpinnerDateModel model = new SpinnerDateModel(
                parsedDate,
                null,
                null,
                Calendar.DAY_OF_MONTH 
            );
            dateSpinner.setModel(model);
            dateSpinner.setEditor(new JSpinner.DateEditor(dateSpinner, "dd-MMM-yyyy"));
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(
                null,
                "Invalid date. Use format: dd-MMM-yyyy (e.g., 29-Oct-2023)",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }

