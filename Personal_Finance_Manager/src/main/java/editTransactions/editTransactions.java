    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed
        try {
            Object selectedItem = jComboBox3.getSelectedItem();
            if (selectedItem == null || selectedItem.toString().trim().isEmpty()) {
                return;
            }
    
            String selectedCategory = selectedItem.toString();
            if ("-- Select a Category --".equals(selectedCategory) || 
                "-- Categories not found --".equals(selectedCategory)) {
                return;
            }
            jTextField3.setText(String.format("%d", ETbl.getCategoryIDbyName(jComboBox3, sessionData)));
            jTextField4.setText(selectedCategory);
            if (ETbl.getCategoryType(jComboBox3, sessionData)) {
                jComboBox4.setSelectedIndex(0);
            } else {
                jComboBox4.setSelectedIndex(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(editTransactions.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jComboBox3ActionPerformed

